class Gem {
  color: any;
  sprite: any;
  value: any;

  getColor() {
    return this.color;
  }

  getSprite() {
    return this.sprite;
  }

  getValue() {
    return this.value;
  }

  setColor(color: any) {
    this.color = color;
  }

  setSprite(sprite: any) {
    this.sprite = sprite;
  }

  setValue(value: any) {
    this.value = value;
  }
}
